    private void txtNIMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNIMActionPerformed
        String nmprogdi = null, progdi = txtNIM.getText().substring(0,3);
        int pilih;
        if (progdi.equalsIgnoreCase("A22")) {
            nmprogdi="TI - D3";
            pilih=1;
        }else if (progdi.equalsIgnoreCase("A24")) {
            nmprogdi = "Broadcasting - D3";
            pilih = 1;
        }else{
            txtNama.setEnabled(false);
            JOptionPane.showMessageDialog(null, "NIM salah, ulangi ...", "peringatan", 1);
            pilih =0;
            txtNIM.requestFocus();
        }
        if (pilih==1) {
            txtProdi.setText(nmprogdi);
            txtNama.setEnabled(true);
            txtNama.requestFocus();
        }
        btnBersih.setEnabled(true);
        btnBatal.setEnabled(true);
        btnBaru.setEnabled(false);
    }//GEN-LAST:event_txtNIMActionPerformed

