    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        double bobot , bobotsks=0, bobottotal=0, ips;
        int brs=0 ,sks=0, skstotal = 0;
        String huruf;
        DefaultTableModel dataModel = (DefaultTableModel) tabelMatkul.getModel();
        brs=tabelMatkul.getSelectedRow();
        if(brs>=0){
            int opsi=JOptionPane.showConfirmDialog(null, "Yakin Dihapus ?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (opsi==JOptionPane.YES_OPTION){
                dataModel.removeRow(brs);
                int jmlbaris = dataModel.getRowCount();
                if (jmlbaris<=0){
                    txtSkstotal.setText("");
                    txtIPS.setText("");
                }else {
                    for (int i= 0; i <jmlbaris ; i++){
                        // menghitung SKS
                        sks = Integer.valueOf(dataModel.getValueAt(i, 1).toString());
                        skstotal = skstotal+sks;
                        // menghitung IPS
                        huruf = dataModel.getValueAt(i, 7).toString();
                        if(huruf.equals("A")){
                            bobot = 4.0;
                        }else if(huruf.equals("AB")){
                            bobot = 3.5;
                        }else if(huruf.equals("B")){
                            bobot = 3.0;
                        }else if(huruf.equals("BC")){
                            bobot = 2.5;
                        }else if(huruf.equals("C")){
                            bobot = 2.0;
                        }else if(huruf.equals("D")){
                            bobot = 1.0;
                        }else{
                            bobot = 0.0;
                        }
                        bobotsks = bobot*sks;
                        bobottotal = bobottotal + bobotsks;
                    }
                    txtSkstotal.setText(String.valueOf(skstotal));
                    ips= bobottotal / skstotal;
                    txtIPS.setText(String.valueOf(ips));
                    // Clear
                    txtSKS.setText("");
                    txtJenis.setText("");
                    txtTugas.setText("");
                    txtUTS.setText("");
                    txtUAS.setText("");
                    txtAkhir.setText("");
                    txtHuruf.setText("");
                    txtTugas.setEnabled(false);
                    txtUTS.setEnabled(false);
                    txtUAS.setEnabled(false);
                    btnTambah.setEnabled(false);
                    btnHapus.setEnabled(true);
                    btnBatal.setEnabled(true);
                    cmbMatkul.setSelectedIndex(0);
                    cmbMatkul.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

