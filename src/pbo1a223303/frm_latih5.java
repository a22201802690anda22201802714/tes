    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        DefaultTableModel dataModel = (DefaultTableModel) tabelMatkul.getModel();
        List list = new ArrayList<>();
        tabelMatkul.setAutoCreateColumnsFromModel(true);
        list.add(cmbMatkul.getSelectedItem());
        list.add(txtSKS.getText());
        list.add(txtJenis.getText());
        list.add(txtTugas.getText());
        list.add(txtUTS.getText());
        list.add(txtUAS.getText());
        list.add(txtAkhir.getText());
        list.add(txtHuruf.getText());
        dataModel.addRow(list.toArray());
        double bobot, bobotsks=0, bobottotal=0, ips, skstotal=0, sks;
        String huruf;
        int jmlbaris = dataModel.getRowCount();
        for (int i = 0; i < jmlbaris; i++) {
            sks=Integer.valueOf(dataModel.getValueAt(i, 1).toString());
            skstotal=skstotal+sks;
            //menghitung ips
            huruf=dataModel.getValueAt(i,7).toString();
            if (huruf.equals("A")) {
                bobot=4.0;
            }else if (huruf.equals("AB")) {
                bobot=3.5;
            }else if (huruf.equals("B")) {
                bobot=3.0;
            }else if (huruf.equals("BC")) {
                bobot=2.5;
            }else if (huruf.equals("C")) {
                bobot=2.0;
            }else if (huruf.equals("D")) {
                bobot=1.0;
            }else{
                bobot=0.0;
            }
            bobotsks=bobot*sks;
            bobottotal=bobottotal+bobotsks;
        }
        txtSkstotal.setText(String.valueOf(skstotal));
        ips=bobottotal/skstotal;
        txtIPS.setText(String.valueOf(ips));
        txtSKS.setText("");
        txtJenis.setText("");
        txtTugas.setText("");
        txtUTS.setText("");
        txtUAS.setText("");
        txtAkhir.setText("");
        txtHuruf.setText("");
        txtTugas.setEnabled(false);
        txtUTS.setEnabled(false);
        txtUAS.setEnabled(false);
        btnTambah.setEnabled(false);
        cmbMatkul.requestFocus();
        btnHapus.setEnabled(true);
        btnBatal.setEnabled(true);
    }//GEN-LAST:event_btnTambahActionPerformed

